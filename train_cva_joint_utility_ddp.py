#!/usr/bin/env python3
"""Distributed training for the evaluator-aligned CVA joint utility scorer.

The base EconomicGrasp-DPT/CVA network is not instantiated here. Candidate
features and exact evaluator labels are loaded from the cache generated by the
joint-utility miner.

Important integration detail
----------------------------
Some EconomicGrasp packages import ``utils.arguments`` from their package
``__init__`` files. That module parses ``sys.argv`` at import time. Therefore
this script must consume and remove its own ``--cache_dir``, ``--output_dir``,
and scorer-specific arguments *before* importing any project-local package.
Otherwise the legacy EconomicGrasp parser sees these arguments and exits with
"unrecognized arguments".
"""
